    <br>
    <div style="padding:0px 500px;">
    <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;凡能引起腹泻,或滑润大肠,促进排便的药物,称为泻下药。</div>
    <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本类药物具有泻下通便、清热泻火、逐水退肿等作用,主要用于大便秘结、胃肠积滞、实热内结、水肿停饮等里实证。</div>
    <br>
    <div>根据本类药物的特点及使用范围的不同,可分为三类。</div>
    <br>
    <div>1.攻下药:本类药物多苦寒,其性沉降,主入胃、大肠经。具有较强的泻下通便作用,并能清热泻火。</div>
    <br>
    <div>2.润下药:本类药物多为种子或种仁,富含油脂,能滑润大肠,使大便软化易于排出,适于肠燥津枯便秘。</div>
    <br>
    <div>3.峻下逐水药:本类药物苦寒有毒,泻下力峻猛,药后引起剧烈腹泻,使体内潴留的水液从大便排出。</div>
    <br>
    年老体弱、脾胃虚弱慎用；妇女胎前产后经期忌用；应用有毒峻下药要严格控制用量。
