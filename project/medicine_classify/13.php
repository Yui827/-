    <br>
    <div style="padding:0px 500px;">
    <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;凡能补益正气,增强体质,以提高抗病能力,治疗虚正证为主的药物,称为补虚药。</div>
    <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本类中药具有补益人体气、血、阴、阳的作用。主要用于气虚、血虚、阴虚、阳虚等虚证。</div>
    <br>
    <div>1.补气药:本类药物性味多甘温或甘平,能补益脏腑之气。主要适用于脾气虚引起的神疲乏力,食欲不振,脘腹虚胀,大便溏薄,浮肿,脱肛和肺气虚引起的少气懒眼言,语音低微,喘促,易出虚汗等症。</div>
    <br>
    <div>2.补阳药:本类药物性味多甘温或咸温或辛热,能温补人体之阳气。主要适用于肾阳不足的怯寒肢冷,腰膝酸软,阳萎早泄,宫冷不孕,尿频遗尿等证。</div>
    <br>
    <div>3.补血药:本类药物性味多甘温或甘平,质地滋润,能补肝养心或益脾。主要适用于心肝血虚所致的面色萎黄,唇爪苍白,眩晕耳鸣,失眠健忘等证。</div>
    <br>
    <div>4.补阴药:本类药物药性多甘寒质润,能补阴、滋液、润燥,而以治疗阴虚液亏之证。阴虚证多见热病后期及若干慢性疾病。</div>
    <br>
    解表药多为辛散之品,入汤剂不宜久煎,以免有效成分挥发而降低疗效。
