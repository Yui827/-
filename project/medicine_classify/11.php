    <div>1.固表止汗药:本类药物能行肌表,调节卫分,而有固表敛汗的功效。</div>
    <br>
    <div>2.敛肺涩肠药:本类药物酸涩收敛,主入肺经或大肠经。分别具有敛肺止咳喘和涩肠止泻痢作用。</div>
