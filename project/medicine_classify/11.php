    <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本类药物味多酸涩,性温或平,主入肺、脾、肾、大肠经。具有固表止汗、敛肺止咳、涩肠止泻、收敛止血等作用,主要用于久病体虚、元气不固所引起的自汗、盗汗、久咳、久泻、久痢、脱肛、遗精、早泄、遗尿、尿频、带下日久、失血崩漏等滑脱不禁的病证。根据药物功效特点分为三类。</div>
